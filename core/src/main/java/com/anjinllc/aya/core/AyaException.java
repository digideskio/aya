package com.anjinllc.aya.core;

/**
 * @author Bryan Turner
 * @since 0.1
 */
public class AyaException extends RuntimeException {
    public AyaException(String message) {
        super(message);
    }

    public AyaException(String message, Throwable cause) {
        super(message, cause);
    }

    public AyaException(Throwable cause) {
        super(cause);
    }
}
